ShimmerLoader(
    {required double width,
    double? height,
    Color? color,
    Decoration? decoration}) {
  return Shimmer.fromColors(
    baseColor: Colors.grey[300]!,
    highlightColor: Colors.grey[100]!,
    child: Container(
      decoration: decoration,
      width: width,
      height: height ?? 10,
      color: color ?? Colors.white,
    ),
  );
}

